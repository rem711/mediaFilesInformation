    private void buttonOpenFileChooserMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_buttonOpenFileChooserMouseReleased
        if(this.fileChooser.showOpenDialog(this) == javax.swing.JFileChooser.APPROVE_OPTION) {
            this.folder = this.fileChooser.getSelectedFile();
        }
        else {
            this.folder = null;
        }
        
        if(this.folder != null && this.folder.isDirectory()) {
            this.labelStatus.setText(this.STATUS_GETTING_INFO);
            this.labelStatus.repaint();
                        
            this.fileExplorer.setInitFolder(this.folder);
            this.tabChrono[this.TIME_GETTING_INFO].setBegin();
            this.fileExplorer.populate();
            this.tabChrono[this.TIME_GETTING_INFO].setEnd();
            List<Media> medias = this.fileExplorer.getListMedias();
            
            if(medias.isEmpty()) {
                this.labelStatus.setText(this.NO_FILE);
                this.labelStatus.repaint();
            }
            else {
                this.labelStatus.setText(this.STATUS_GENERATING_FILES);
                this.labelStatus.repaint();
                this.labelInfo.setText(medias.size() + " file(s) found in " + this.tabChrono[this.TIME_GETTING_INFO].difference() + "s");
                this.labelInfo.repaint();
            }
        }
    }//GEN-LAST:event_buttonOpenFileChooserMouseReleased

